package com.eno.baozi.wenjuan.questionslove.copa;

public class AvgAndStd {
    double avg;
    double std;

    AvgAndStd(double avg,double std){
        this.avg = avg;
        this.std = std;
    }

    public double getAvg() {
        return avg;
    }

    public void setAvg(double avg) {
        this.avg = avg;
    }

    public double getStd() {
        return std;
    }

    public void setStd(double std) {
        this.std = std;
    }
}

